# 1.18
# 思路：规律是每个元素等于索引值和索引值加1相乘
# 1.19
# 思路：26个小写字母对应的ascii的值是97～122，使用内置函数chr
#######################################################


def c_118():
    return [i*(i+1) for i in range(10)]


def c_119():
    return [chr(i) for i in range(97, 123)]


def test():
    print(f"C_1.18 result {c_118()}")
    print(f"C_1.19 result {c_119()}")


if __name__ == '__main__':
    test()
